    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection connect = (Connection) DriverManager.getConnection("jdbc:mysql://localhost/agenciaautos?" + "user=root&password=");

            PreparedStatement ps;
            int id = Integer.parseInt(txt_busca.getText());
            String busca = "select * from clientes WHERE id=" + id;
            ps = (PreparedStatement) connect.prepareStatement(busca);
            ResultSet rs = ps.executeQuery();
            rs.next();
            txt_nombrecliente.setText(rs.getString("nombre"));
            txt_direccioncliente.setText(rs.getString("direccion"));
            txt_telefonocliente.setText(rs.getString("telefono"));
            txt_sexocliente.setText(rs.getString("sexo"));

            ps.close();
            rs.close();

        } catch (SQLException ex) {
            Logger.getLogger(ModificarCliente.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ModificarCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

